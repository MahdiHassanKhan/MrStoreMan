    private void jButtonInsertIntoEmployeeTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertIntoEmployeeTableActionPerformed
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=MrStoreMan;selectMethod=cursor", "sa", "123456");
            //Statement stLogin = connectionLogin.createStatement();
            //String sqlLogin = "select * from Owner where OwnerEmail = ? and OwnerPassword = ?";
            //PreparedStatement pstLogin = connectionLogin.prepareStatement(sqlLogin);
            Date date = new Date();
            java.sql.Date joiningdate = new java.sql.Date(date.getTime());
            String sqlInsert = "insert into Employee"
            +"(EmployeeName,EmployeeContact,EmployeeEmail,EmployeeJoiningDate,EmployeeSalary,EmployeeQuittingDate,EmployeePassword,EmployeeLocation)"
            +"values(?,?,?,?,?,?,?,?)";
            
            PreparedStatement pst = con.prepareStatement(sqlInsert);
            pst.setString(1, EmpName.getText());
            pst.setString(2, EmpContact.getText());
            pst.setString(3, EmpEmail.getText());
            pst.setDate(4, joiningdate);
            pst.setString(5, EmpSalary.getText());
            pst.setString(6, "In Contract");
            pst.setString(7, EmpPassword.getText());
            pst.setString(8, EmpLocation.getText());

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Insert Successfully");

            //connectionLogin.close();

        }
        catch (Exception e) {
            //e.printStackTrace();
            //System.out.println(e.getMessage());
            //JOptionPane.showMessageDialog(null,e);
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jButtonInsertIntoEmployeeTableActionPerformed


    private void jButtonInsertIntoORRTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertIntoORRTableActionPerformed
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=MrStoreMan;selectMethod=cursor", "sa", "123456");
            //Statement stLogin = connectionLogin.createStatement();
            //String sqlLogin = "select * from Owner where OwnerEmail = ? and OwnerPassword = ?";
            //PreparedStatement pstLogin = connectionLogin.prepareStatement(sqlLogin);
            String sqlInsert = "insert into OwnerRegistrationRequests"
            +"(OwnerName,OwnerContact,OwnerEmail,OwnerPassword,OwnerLocation)"
            +"values(?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sqlInsert);
            pst.setString(1, OwnerNameR.getText());
            pst.setString(2, OwnerContactR.getText());
            pst.setString(3, OwnerEmailR.getText());
            pst.setString(4, OwnerPasswordR.getText());
            pst.setString(5, OwnerLocationR.getText());
            if(OwnerLocationR.getText().length()>0)
            {
                pst.setString(5, OwnerLocationR.getText());
            }
            else
            {
                 pst.setString(5, "Dhaka");
            }

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Registration Completed as an Owner. Wait for approval");
            OwnerNameR.setText("");
            OwnerContactR.setText("");
            OwnerEmailR.setText("");
            OwnerPasswordR.setText("");
            OwnerLocationR.setText("");

            //connectionLogin.close();

        }
        catch (Exception e) {
            //e.printStackTrace();
            //System.out.println(e.getMessage());
            //JOptionPane.showMessageDialog(null,e);
            //JOptionPane.showMessageDialog(this,e.getMessage());
            JOptionPane.showMessageDialog(this,"Someone already registered with same Contact or email. Try Again.");
        }
    }//GEN-LAST:event_jButtonInsertIntoORRTableActionPerformed


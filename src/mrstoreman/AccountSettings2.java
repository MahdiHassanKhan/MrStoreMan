    private void jButtonUpdateFromEmployeeTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateFromEmployeeTableActionPerformed

        int passwordmatched=0;
        int retypematched=0;

        String name = UserName.getText();
        String contact = UserContact.getText();
        String email = UserEmail.getText();
        String location = UserLocation.getText();

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=MrStoreMan;selectMethod=cursor", "sa", "123456");

            String sqlInsert = "update Employee set EmployeeName=?,EmployeeContact=?,EmployeeEmail=?,EmployeePassword=?,EmployeeLocation=? where EmployeeId=?";
            PreparedStatement pst = con.prepareStatement(sqlInsert);

            if(UserName.getText().length()>0)
            {
                pst.setString(1, UserName.getText());
            }
            else
            {
                pst.setString(1, name);
            }

            if(UserContact.getText().length()>0)
            {
                pst.setString(2, UserContact.getText());
            }
            else
            {
                pst.setString(2, contact);
            }

            if(UserEmail.getText().length()>0)
            {
                pst.setString(3, UserEmail.getText());
            }
            else
            {
                pst.setString(3, email);
            }

            //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

            System.out.println();

            if(UserCurrentPass.getText().length()>0&& UserCurrentPass.getText().equals(jLabelUserCP.getText()))
            {
                //pst.setString(4, UserCurrentPass.getText());
                passwordmatched=1;
                System.out.println("if e");
            }
            else
            {
                System.out.println("else e");
                passwordmatched=0;
                if(passwordmatched==0)
                {
                    JOptionPane.showMessageDialog(this,"Current Password didn't match");
                }
            }

            if(passwordmatched==1 && UserNewPass.getText().length()==0 && UserRetypePass.getText().length()==0)
            {
                pst.setString(4, UserCurrentPass.getText());
            }

            else if(passwordmatched==1 && (UserNewPass.getText().length()>0 || UserRetypePass.getText().length()>0) && UserNewPass.getText().equals(UserRetypePass.getText()))
            {
                pst.setString(4, UserNewPass.getText());
                retypematched=1;
            }

            else if(passwordmatched==1 && (UserNewPass.getText().length()>0 || UserRetypePass.getText().length()>0) && !(UserNewPass.getText().equals(UserRetypePass.getText())))
            {
                retypematched=0;
                if(retypematched==0)
                {
                    JOptionPane.showMessageDialog(this,"New Password & Re-Type Password didn't match");
                }
            }

            /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

            if(UserLocation.getText().length()>0)
            {
                pst.setString(5, UserLocation.getText());
            }
            else
            {
                pst.setString(5, location);
            }

            pst.setString(6, UserID.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this,"User profile changes saved successfully");

            /*if(UEIID.getText().length()==0)
            {
                JOptionPane.showMessageDialog(this,"Nothing updated");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Successfully updated");
            }
            //JOptionPane.showMessageDialog(this,"Successfully updated");

            //connectionLogin.close();*/
        }

        catch (Exception e) {
            //e.printStackTrace();
            //System.out.println(e.getMessage());
            //JOptionPane.showMessageDialog(null,e);
            if(passwordmatched==0||retypematched==0)
            {
                JOptionPane.showMessageDialog(this,"No changes saved");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Entered Email or Contact belongs to another user");
            }
        }
    }//GEN-LAST:event_jButtonUpdateFromEmployeeTableActionPerformed

